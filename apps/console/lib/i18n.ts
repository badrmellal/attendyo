/**
 * Translate a UI chrome key for the locale, with optional `{token}`
 * interpolation. French is the ultimate fallback (the app is French-first);
 * an unknown key returns the key itself so a missing string is visible, not
 * blank.
 */
export function t(
  locale: Locale,
  key: string,
  params?: Record<string, string | number>,
): string {
  const raw = STRINGS[locale]?.[key] ?? STRINGS.fr[key] ?? key;
  if (!params) return raw;
  return raw.replace(/\{(\w+)\}/g, (_, name: string) =>
    name in params ? String(params[name]) : `{${name}}`,
  );
}

